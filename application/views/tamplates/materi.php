<h2>Dasar Jaringan Komputer</h2>
	<h3>Jaringan komputer adalah koneksi antara dua device atau lebih, yang terhubung secara fisik maupun secara logika sehingga bisa saling bertukar informasi.</h3>
	<h2>Jenis jaringan komputer berdasarkan Transmisi antara lain:</h2>
		<li>Jaringan Brodcast memiliki saluran komunikasi tunggal yang dipakai bersama-sama oleh semua device yang terkoneksi ke jaringan.</li>
		<li>Jaringan Point to Point terdiri dari beberapa koneksi pasangan individu, dari satu device ke satu device lain. Untuk mengirim paket pada jaringan jenis ini mungkin harus melalui satu atau lebih mesin-mesin perantara.</li>
	<h2>Berdasarkan Geografis :</h2>
		<li>Local Area Network (LAN) didefinisikan sebagai kumpulan komputer yang saling dihubungkan bersama di dalam satu area tertentu yang tidak begitu luas, seperti di dalam satu kantor atau gedung.</li>
		<li>Metropolitan Area Network (MAN) merupakan versi LAN yang berukuran lebih besar dan biasanya memakai teknologi yang sama dengan LAN. MAN dapat mencakup kantor-kantor perusahaan yang berdekatan dan dapat dimanfaatkan untuk keperluan pribadi (swasta) atau umum.</li>
		<li>Wide Area Network(WAN) merupakan jaringan komputer yang mencakup daerah geografis yang luas, sering kali mencakup sebuah negara atau benua.</li>
		<li>Internet adalah seluruh jaringan komputer yang saling terhubung menggunakan standar sistem global Transmission Control Protocol/Internet Protocol Suite (TCP/IP) sebagai protokol pertukaran paket (packet switching communication protocol)</li>
