<h2>Database</h2>
	<h3>DataBase merupakan Kumpulan informasi yang dsimpan di dalam komputer secara
	sistematik sehingga dapat diperiksa menggunakan suatu program komputer untuk memperoleh informasi
	dari database tersebut.</h3>
	<h2>Tujuan Database antara lain:</h2>
	1. Kecepatan dan kemudahan (Speed) agar pengguna database dapat menyimpan data, melakukan perubahan
	/manipulasi terhadap data, dan menampilkan kembali data dengan lebih cepat dan mudah dibandingkan dengan
	cara biasa (baik manual ataupun elektronis).
	<p>
	2. Efisiensi ruang penyimpanan (Space) Dengan database kita mampu melakukan penekanan jumlah pengulangan
	data, baik dengan menerapkan sejumlah pengkodean atau dengan membuat relasi-relasi antara kelompok data
	yang saling berhubungan.
	</p>
	<p>
	3. Keakuratan (Accuracy) Agar data sesuai dengan aturan dan batasan tertentu dengan cara memanfaatkan 
	pengkodean atau pembentukan relasi antar data bersama dengan penerapan aturan/batasan (constraint) tipe data,
	domain data, keunikan data, dsb.
	</p>
	<p>
	4. Keamanan (Security) Agar data yang bersifat rahasia atau proses yang vital tidak jatuh ke orang/pengguna yang tidak berhak, yakni dengan penggunaan account (username dan password) serta menerapkan pembedaan hak akses setiap pengguna terhadap data yang bisa dibaca atau proses yang bisa dilakukan.
	</p>
